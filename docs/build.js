#!/usr/bin/env node
/**
 * docs/build.js
 * Scans all SVG icons and generates docs/icons-data.js
 * Run: node docs/build.js   (from repo root)
 */

"use strict";

const fs   = require("fs");
const path = require("path");

const ROOT      = path.join(__dirname, "..");
const ICONS_DIR = path.join(ROOT, "icons");
const META_FILE = path.join(ROOT, "metadata.json");
const OUTPUT    = path.join(__dirname, "icons-data.js");

if (!fs.existsSync(ICONS_DIR)) {
  console.error(`ERROR: icons/ not found at ${ICONS_DIR}`);
  process.exit(1);
}

// ── Load metadata ─────────────────────────────────────────────────────────────
let meta = { icons: [], categories: {}, version: "1.0.0" };
if (fs.existsSync(META_FILE)) {
  meta = JSON.parse(fs.readFileSync(META_FILE, "utf8"));
}

const metaLookup = {};
for (const icon of meta.icons) {
  metaLookup[icon.name] = icon;
}

// ── Scan SVG files ────────────────────────────────────────────────────────────
const icons      = [];
const categories = fs
  .readdirSync(ICONS_DIR, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

for (const category of categories) {
  const dir   = path.join(ICONS_DIR, category);
  const files = fs
    .readdirSync(dir)
    .filter((f) => f.toLowerCase().endsWith(".svg"))
    .sort();

  for (const file of files) {
    const name       = path.parse(file).name;
    const svgContent = fs.readFileSync(path.join(dir, file), "utf8").trim();
    const iconMeta   = metaLookup[name] || {};

    icons.push({
      name,
      file,
      category,
      tags: iconMeta.tags || [],
      svg:  svgContent,
    });
  }
}

// ── Write output ──────────────────────────────────────────────────────────────
const categoryMeta = {};
for (const cat of categories) {
  categoryMeta[cat] = meta.categories?.[cat] || {
    label: cat.charAt(0).toUpperCase() + cat.slice(1),
    count: icons.filter((i) => i.category === cat).length,
  };
}

const js = `// Auto-generated by docs/build.js — do not edit manually.
// Generated: ${new Date().toISOString()}
// Run "node docs/build.js" from the repo root to regenerate.

window.ICONS_DATA = ${JSON.stringify(icons, null, 2)};

window.ICONS_META = ${JSON.stringify(
  {
    version:          meta.version || "1.0.0",
    totalIcons:       icons.length,
    totalCategories:  categories.length,
    categories:       categoryMeta,
    generatedAt:      new Date().toISOString(),
  },
  null,
  2
)};
`;

fs.writeFileSync(OUTPUT, js, "utf8");

console.log(`\nNorthline Icons — Build Complete`);
console.log(`  Icons     : ${icons.length}`);
console.log(`  Categories: ${categories.length}`);
console.log(`  Output    : ${OUTPUT}`);
console.log();
